#include <stdio.h>

char	*ft_strrchr(const char *s, int c)
{
	char	*b;
	int		i;

	i = 0;
	b = (char *) s;
	while (b[i] != '\0')
		i++;
	i--;
	while (i >= 0)
	{
		if (b[i] == c)
			return (&b[i]);
		i--;
	}
	return (0);
}
